    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        switch (juego) {
            case 1:
                if (jTextField1.getText().equalsIgnoreCase("argentina")) {
                    JOptionPane.showMessageDialog(null,"Usted ha ganado!");
                    jButton4.setEnabled(true);
                    jButton3.setEnabled(false);
                }else{
                    vidas++;
                }              
                break;               
                case 2:                   
                if (jTextField1.getText().equalsIgnoreCase("colombia")) {
                    JOptionPane.showMessageDialog(null,"Usted ha ganado!");
                    jButton4.setEnabled(true);
                    jButton3.setEnabled(false);
                }else{
                    vidas++;
                }               
                break;
                case 3:
                if (jTextField1.getText().equalsIgnoreCase("alemania")) {
                    JOptionPane.showMessageDialog(null,"Usted ha ganado!");
                    jButton4.setEnabled(true);
                   jButton3.setEnabled(false);
                }else{
                    vidas++;
                }               
                break;
                case 4:
                if (jTextField1.getText().equalsIgnoreCase("islandia")) {
                    JOptionPane.showMessageDialog(null,"Usted ha ganado!");
                    jButton4.setEnabled(true);
                    jButton3.setEnabled(false);
                }else{
                    vidas++;
                }               
                break;
                case 5:
                if (jTextField1.getText().equalsIgnoreCase("noruega")) {
                    JOptionPane.showMessageDialog(null,"Usted ha ganado!");
                    jButton4.setEnabled(true);
                   jButton3.setEnabled(false);
                }else{
                    vidas++;
                }               
                break;
                case 6:
                if (jTextField1.getText().equalsIgnoreCase("turquia")) {
                    JOptionPane.showMessageDialog(null,"Usted ha ganado!");
                    jButton4.setEnabled(true);
                    jButton3.setEnabled(false);
                }else{
                    vidas++;
                }               
                break;
                case 7:
                if (jTextField1.getText().equalsIgnoreCase("dinamarca")) {
                    JOptionPane.showMessageDialog(null,"Usted ha ganado!");
                    jButton4.setEnabled(true);
                   jButton3.setEnabled(false);
                }else{
                    vidas++;
                }               
                break;                          
        }
        
        
        switch (vidas) {
            case 1:
                jLabel17.setEnabled(false);
                break;
                case 2:
                jLabel16.setEnabled(false);
                break;
                case 3:
                jLabel4.setEnabled(false);
                break;          
        }
        
        if (vidas >= 3){
            JOptionPane.showMessageDialog(null, "Usted ha perdido");
            jButton3.setEnabled(false);
            jButton4.setEnabled(true);
        }
       
        
    }//GEN-LAST:event_jButton3ActionPerformed

