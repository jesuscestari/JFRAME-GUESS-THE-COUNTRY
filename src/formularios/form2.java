    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
      JOptionPane.showMessageDialog(null,"Introduce el nombre del país y presione verificar");     
      jLabel4.setIcon(new ImageIcon(getClass().getResource("/img/corazon.png")));
      jLabel16.setIcon(new ImageIcon(getClass().getResource("/img/corazon.png")));
      jLabel17.setIcon(new ImageIcon(getClass().getResource("/img/corazon.png")));
      jLabel17.setEnabled(true);
      jLabel16.setEnabled(true);
      jLabel4.setEnabled(true);
      jTextField1.setText("");
      jButton4.setEnabled(false);
      jButton3.setEnabled(true);
     
        banderas[0]="argentina";     
        banderas[1]="colombia";
        banderas[2]="islandia";
        banderas[3]="noruega";
        banderas[4]="dinamarca";
        banderas[5]="turquia";
        banderas[6]="alemania";       
        
        banderarnd = rnd.nextInt(banderas.length);
        System.out.println("Indice del array:"+banderarnd);
        
        paisElegido = banderas[banderarnd];
        System.out.println("Bandera elegida:"+paisElegido);
        
        if (paisElegido.equalsIgnoreCase("argentina") ) {           
            jLabel2.setIcon(new ImageIcon(getClass().getResource("/img/argentina.png")));
            juego = 1;
        }else if (paisElegido.equalsIgnoreCase("colombia")) {
            jLabel2.setIcon(new ImageIcon(getClass().getResource("/img/colombia.png")));
            juego = 2;
        }else if (paisElegido.equalsIgnoreCase("alemania")) {
            jLabel2.setIcon(new ImageIcon(getClass().getResource("/img/alemania.png")));
            juego = 3;
        }else if (paisElegido.equalsIgnoreCase("islandia")) {
            jLabel2.setIcon(new ImageIcon(getClass().getResource("/img/islandia.png")));
            juego = 4;
        }else if (paisElegido.equalsIgnoreCase("noruega")) {
            jLabel2.setIcon(new ImageIcon(getClass().getResource("/img/noruega.png")));
            juego = 5;
        }else if (paisElegido.equalsIgnoreCase("turquia")) {
            jLabel2.setIcon(new ImageIcon(getClass().getResource("/img/turquia.png")));
            juego = 6;
        }else if (paisElegido.equalsIgnoreCase("dinamarca")) {
            jLabel2.setIcon(new ImageIcon(getClass().getResource("/img/dinamarca.png")));
            juego = 7;
        }
        vidas = 0;
        
         
    }//GEN-LAST:event_jButton4ActionPerformed
            
